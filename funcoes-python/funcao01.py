def exibir():
    print("Executando a função exibir()")
    print("Execução concluída") # Tudo que encontrar dentro da função, deve estar indentado, ou seja, deve ter um recuo. 

exibir() # Para executar a função, é necessário chamar o nome da função seguido de parênteses. Se não for chamado, a função não será executada.
print("Após a execução da função exibir()") # O código fora da função será executado normalmente. Mas depois da função exibir() ser executada.

