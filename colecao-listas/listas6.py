# posições
#        0   1   2   3   4   5
lista = [10, 20, 30, 40, 50, 60]
print("Lista antes da modificação")
print(lista)

lista[2] = 67

print("Lista depois da modificação")
print(lista)

# Listas são mutáveis, você pode facilmente altrar os elementos de uma lista utilizando o índice. No exemplo acima, o elemento na posição 2 (que era 30) foi alterado para 67.
