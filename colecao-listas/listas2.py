lista_mista = [14, "Guisa", 7.5]


print("Lista Inteira")
print(lista_mista)

print("Posições positivas")
print(lista_mista[0]) # Acessando o primeiro elemento da lista, lembrando que a contagem começa do zero.

print("Posições negativas")
print(lista_mista[-1]) # Acessando o último elemento da lista, utilizando o índice -1.
print(lista_mista[-2]) # Acessando o penúltimo elemento da lista, utilizando o índice -2.

