#              0         1        2
animais = ["Cachorro", "Gato", "Leão "]
print(animais)

print("Posições positivas")
print(animais[0]) # Acessando o primeiro elemento da lista, lembrando que a contagem começa do zero.
print(animais[1])

print("Posições negativas")
print(animais[-1]) # Acessando o último elemento da lista, utilizando o índice -1.
print(animais[-3])

cachorro = animais[0] # Atribuindo o valor do primeiro elemento da lista a uma variável.
print(cachorro) 
